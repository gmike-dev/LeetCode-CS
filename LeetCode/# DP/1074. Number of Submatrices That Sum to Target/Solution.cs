namespace LeetCode.__DP._1074._Number_of_Submatrices_That_Sum_to_Target;

public class Solution
{
  public int NumSubmatrixSumTarget(int[][] matrix, int target)
  {
    var m = matrix.Length;
    var n = matrix[0].Length;
    var s = CreateMatrix(m + 1, n + 1);
    for (var i = 1; i <= m; i++)
    {
      for (var j = 1; j <= n; j++)
        s[i][j] = s[i - 1][j] + s[i][j - 1] - s[i - 1][j - 1] + matrix[i - 1][j - 1];
    }

    var cnt = CreateMatrix(m + 1, n + 1);
    for (var r1 = 1; r1 <= m; r1++)
    for (var c1 = 1; c1 <= n; c1++)
    for (var r2 = r1; r2 <= m; r2++)
    for (var c2 = c1; c2 <= n; c2++)
    {
      var sum = s[r2][c2] - s[r2][c1 - 1] - s[r1 - 1][c2] + s[r1 - 1][c1 - 1];
      if (sum == target)
        cnt[r1][c1]++;
    }
    var result = 0;
    for (var i = 1; i <= m; i++)
    for (var j = 1; j <= n; j++)
      result += cnt[i][j];
    return result;
  }

  private static int[][] CreateMatrix(int h, int w)
  {
    var mx = new int[h][];
    for (var i = 0; i < h; i++)
      mx[i] = new int[w];
    return mx;
  }
}

