namespace LeetCode.__DP._1074._Number_of_Submatrices_That_Sum_to_Target;

public class PrefixColumnsSumSolution
{
  public int NumSubmatrixSumTarget(int[][] matrix, int target)
  {
    var m = matrix.Length;
    var n = matrix[0].Length;
    var s = new int[m][];
    for (var i = 0; i < m; i++)
      s[i] = new int[n + 1];
    for (var i = 0; i < m; i++)
    {
      for (var j = 1; j <= n; j++)
        s[i][j] = s[i][j - 1] + matrix[i][j - 1];
    }
    var result = 0;
    for (var i = 1; i <= n; i++)
    {
      for (var j = i; j <= n; j++)
      {
        var count = new Dictionary<int, int> { { 0, 1 } };
        var sum = 0;
        for (var k = 0; k < m; k++)
        {
          sum += s[k][j] - s[k][i - 1];
          result += count.GetValueOrDefault(sum - target, 0);
          count[sum] = count.GetValueOrDefault(sum, 0) + 1;
        }
      }
    }
    return result;
  }
}

