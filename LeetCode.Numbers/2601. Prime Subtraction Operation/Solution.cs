namespace LeetCode.Numbers._2601._Prime_Subtraction_Operation;

public class Solution
{
  public bool PrimeSubOperation(int[] nums)
  {
    var p = GetPrimes(2000);
    var n = nums.Length;
    for (var i = n - 2; i >= 0; i--)
    {
      if (nums[i] < nums[i + 1])
        continue;
      var j = Array.BinarySearch(p, nums[i] - nums[i + 1] + 1);
      if (j < 0)
        j = ~j;
      if (p[j] >= nums[i])
        j--;
      if (j < 0)
        return false;
      nums[i] -= p[j];
      if (nums[i] >= nums[i + 1])
        return false;
    }
    return true;
  }

  private static int[] GetPrimes(int n)
  {
    var p = new bool[n + 1];
    p.AsSpan().Fill(true);
    p[0] = p[1] = false;
    for (var i = 2; i * i < p.Length; i++)
    {
      if (!p[i])
        continue;
      for (var j = i + i; j < p.Length; j += i)
        p[j] = false;
    }
    var result = new List<int>();
    for (var i = 2; i < p.Length; i++)
    {
      if (p[i])
        result.Add(i);
    }
    return result.ToArray();
  }
}

