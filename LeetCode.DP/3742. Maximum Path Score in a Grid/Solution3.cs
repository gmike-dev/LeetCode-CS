using LeetCode.Common;

namespace LeetCode.DP._3742._Maximum_Path_Score_in_a_Grid;

/// <summary>
/// https://leetcode.com/problems/maximum-path-score-in-a-grid
/// </summary>
public class Solution3
{
  public int MaxPathScore(int[][] grid, int k)
  {
    int m = grid.Length;
    int n = grid[0].Length;
    int[][] dp = new int[n + 1][];
    for (int i = 0; i <= n; i++)
    {
      dp[i] = new int[k + 1];
      if (i != 1)
      {
        dp[i].AsSpan().Fill(-1);
      }
    }
    for (int i = 1; i <= m; i++)
    {
      for (int j = 1; j <= n; j++)
      {
        if (i == 1 && j == 1)
        {
          continue;
        }
        int val = grid[i - 1][j - 1];
        int cost = val == 0 ? 0 : 1;
        int score = val;
        for (int x = k; x >= 0; x--)
        {
          if (x - cost < 0)
          {
            dp[j][x] = -1;
          }
          else
          {
            int up = dp[j][x - cost];
            int right = dp[j - 1][x - cost];
            if (up >= 0 || right >= 0)
            {
              dp[j][x] = score + Math.Max(up, right);
            }
            else
            {
              dp[j][x] = -1;
            }
          }
        }
      }
    }
    return dp[n][k];
  }
}

