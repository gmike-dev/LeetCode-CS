using LeetCode.Common;

namespace LeetCode.DP._3742._Maximum_Path_Score_in_a_Grid;

/// <summary>
/// https://leetcode.com/problems/maximum-path-score-in-a-grid
/// </summary>
public class Solution2
{
  public int MaxPathScore(int[][] grid, int k)
  {
    int m = grid.Length;
    int n = grid[0].Length;
    int[,,] dp = new int[m + 1, n + 1, k + 1];
    for (int i = 0; i <= m; i++)
    {
      for (int j = 0; j <= n; j++)
      {
        for (int x = 0; x <= k; x++)
        {
          dp[i, j, x] = -1;
        }
      }
    }
    for (int x = 0; x <= k; x++)
    {
      dp[0, 1, x] = dp[1, 0, x] = 0;
    }
    for (int i = 1; i <= m; i++)
    {
      for (int j = 1; j <= n; j++)
      {
        int val = grid[i - 1][j - 1];
        int cost = val == 0 ? 0 : 1;
        int score = val;
        for (int x = cost; x <= k; x++)
        {
          int up = dp[i - 1, j, x - cost];
          int right = dp[i, j - 1, x - cost];
          if (up >= 0 || right >= 0)
          {
            dp[i, j, x] = score + Math.Max(up, right);
          }
        }
      }
    }
    return dp[m, n, k];
  }
}

