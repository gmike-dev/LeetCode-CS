using LeetCode.Common;

namespace LeetCode.DP._3742._Maximum_Path_Score_in_a_Grid;

/// <summary>
/// https://leetcode.com/problems/maximum-path-score-in-a-grid
/// </summary>
public class Solution
{
  public int MaxPathScore(int[][] grid, int k)
  {
    int m = grid.Length;
    int n = grid[0].Length;
    int[,,] dp = new int[m, n, k + 1];
    for (int i = 0; i < m; i++)
    {
      for (int j = 0; j < n; j++)
      {
        for (int x = 0; x <= k; x++)
        {
          dp[i, j, x] = -2;
        }
      }
    }
    return Dfs(0, 0, k);

    int Dfs(int i, int j, int remainCost)
    {
      int cost = grid[i][j] == 0 ? 0 : 1;
      if (remainCost < cost)
      {
        return -1;
      }
      int score = grid[i][j];
      if (i == m - 1 && j == n - 1)
      {
        return score;
      }
      if (dp[i, j, remainCost] >= -1)
      {
        return dp[i, j, remainCost];
      }
      int down = i + 1 < m ? Dfs(i + 1, j, remainCost - cost) : -1;
      int right = j + 1 < n ? Dfs(i, j + 1, remainCost - cost) : -1;
      int result = -1;
      if (right != -1 || down != -1)
      {
        result = score + Math.Max(right, down);
      }
      dp[i, j, remainCost] = result;
      return result;
    }
  }
}

